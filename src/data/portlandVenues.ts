function compatibilityPriority(source: CuratedVenue, candidate: CuratedVenue): number {
  if (source.kind === 'restaurant') {
    if (candidate.kind === 'park' || candidate.kind === 'activity') return 0
    if (candidate.kind === 'cafe' || candidate.kind === 'dessert') return 1
    return 2
  }
  if (source.kind === 'park' || source.kind === 'activity') {
    if (candidate.kind === 'cafe' || candidate.kind === 'dessert') return 0
    if (candidate.kind === 'restaurant') return 1
    return 2
  }
  if (candidate.kind === 'park' || candidate.kind === 'activity') return 0
  if (candidate.kind === 'restaurant') return 1
  return 2
}

export const portlandVenues: CuratedVenue[] = portlandVenueSeeds.map((item) => {
  if (item.compatibleVenueIds.length) return item

  const compatibleVenueIds = portlandVenueSeeds
    .filter((candidate) => candidate.id !== item.id && candidate.area === item.area && candidate.status === 'active')
    .sort((a, b) => {
      const priorityDifference = compatibilityPriority(item, a) - compatibilityPriority(item, b)
      if (priorityDifference !== 0) return priorityDifference
      const priceDifference = Math.abs(a.priceTierEstimate - item.priceTierEstimate)
        - Math.abs(b.priceTierEstimate - item.priceTierEstimate)
      return priceDifference || a.id.localeCompare(b.id)
    })
    .slice(0, 3)
    .map((candidate) => candidate.id)

  return { ...item, compatibleVenueIds }
})

export const recommendablePortlandVenues = portlandVenues.filter((item) => item.status === 'active')

const staticCaution = 'Curated static record. Verify current hours, menu, availability, accessibility, and reservation details before leaving.'

function mapsUrl(name: string, address?: string): string {
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(`${name} ${address ?? 'Portland Oregon'}`)}`
}

function reviewUrl(name: string): string {
  return `https://www.google.com/search?q=${encodeURIComponent(`${name} Portland Oregon reviews`)}`
}

function venue(
  input: Omit<CuratedVenue, 'mapsUrl' | 'reviewUrl' | 'verifiedAt' | 'status' | 'dietarySignals' | 'alcoholFit' | 'curationTier' | 'compatibleVenueIds'>
    & Partial<Pick<CuratedVenue, 'verifiedAt' | 'status' | 'dietarySignals' | 'alcoholFit' | 'curationTier' | 'compatibleVenueIds'>>,
): CuratedVenue {
  return {
    ...input,
    dietarySignals: input.dietarySignals ?? [],
    alcoholFit: input.alcoholFit ?? (input.kind === 'restaurant' ? 'alcohol-optional' : 'easy-without-alcohol'),
    curationTier: input.curationTier ?? 'current-guide',
    compatibleVenueIds: input.compatibleVenueIds ?? [],
    mapsUrl: mapsUrl(input.name, input.address),
    reviewUrl: reviewUrl(input.name),
    verifiedAt: input.verifiedAt ?? verifiedAt,
    status: input.status ?? 'active',
  }
}

